package com.syntax.class07;

public class IncrementAndDecrement {

	public static void main(String[] args) {
		
		int num=90;
		num=num+1;
		
		System.out.println(num);//91
		
		//another way to increment variable value by 1 is ++
		num++; // +1
		
		System.out.println(num);//92
		
		//to decrement a value of a variable by 1 is --
		
		num--;
		System.out.println(num);//91
		
		int i=10;
		i--; //9
		i--; //8
		System.out.println(i);
		
		//100++; //cannot do this only with variables
		
		
	}

}
